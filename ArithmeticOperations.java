public class ArithmeticOperations {
    public static void main(String[] args) {
        int x = 5, y=10;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
        System.out.println(x++);
        System.out.println(x--);
        System.out.println(++y);
        System.out.println(--y);
    }
}
